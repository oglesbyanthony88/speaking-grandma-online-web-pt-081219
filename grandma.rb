

def speak_to_grandma(shout)
  shout = gets.chomp
  if shout != shout.uppercase
    puts 'HUH?! SPEAK UP, SONNY!'
  elsif shout != shout.uppercase
    puts 'HUH?! SPEAK UP, SONNY!'
  elsif shout == shout.uppercase
    puts "NO, NOT SINCE 1938!"
  else shout == "I LOVE YOU GRANDMA!"
    puts "I LOVE YOU TOO PUMPKIN"
end